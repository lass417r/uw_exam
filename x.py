# ArangoDB connection function

def db(query, type="cursor"):
    try:
        url = f"http://arangodb:8529/_api/{type}"
        res = requests.post( url, json = query )
        return res.json()
    except Exception as ex:
        print("#"*50)
        print(ex)
    finally:
        pass
 

